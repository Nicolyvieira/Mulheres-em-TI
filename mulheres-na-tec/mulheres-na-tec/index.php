                <img src="images/front-end.png">
                <div class="p-carreiras">
                    <h3>O que é?</h3>
                    <p>Front-End é tudo que envolve a parte visível de um site ou aplicação, com a qual os usuários podem interagir. Essa interface gráfica é desenvolvida a partir de linguagens como JavaScript, HTML e CSS — que são as técnicas de base do desenvolvimento web.</p>
                    <h3>O que faz?</h3>
                    <p>Quem trabalha com Front-End é responsável por desenvolver por meio de código uma interface gráfica, normalmente com as tecnologias base da Web (HTML, CSS e JavaScript). Algumas pessoas podem confundir um pouco esse trabalho com o que um designer faz, mas a diferença aqui é que o designer vai utilizar alguma ferramenta visual para desenhar a interface, do Photoshop ao Sketch, e quem faz front-end estará mais próxima do código em si, que irá rodar em um navegador Web como Chrome, Firefox ou Safari.
                        Como estamos na era da experiência do usuário, todos os elementos do Front-End devem ser pensados para uma navegação mais simples, fluida e sem complicações. As interações que precisam de um passo a passo para explicar como funcionam já não têm espaço nesse mercado. É por isso que o profissional deve ter noções de design e de usabilidade, pensando pela ótica do cliente final.
                    </p>
                </div>
            </div>
            <div class="texto-carreiras">
                <h2>Back-End</h2>
                <div class="p-carreiras">
                    <h3>O que é?</h3>
                    <p>O desenvolvedor back-end é o responsável pela estrutura e garante a realização das ações por parte dos usuários em um computador. Esse tipo de desenvolvedor é aquele que está por dentro das linguagens de programação, de desenvolvimento de software e conceitos de CSS e HTML.</p>
                    <h3>O que faz?</h3>
                    <p>Esse tipo de desenvolvedor atua nos “bastidores” de um site, os deixando dinâmico com linguagens de programação, além de organizar as informações o máximo possível para que o usuário tenha uma boa experiência. 
                        Como por exemplo: ao acessar um site, criar um cadastro e enviar seus dados uma espécie de caminho até o armazenamento (banco de dados) é percorrido até seu cadastro ser salvo, para isso acontecer uma série de comandos e códigos acontece até o salvamento dos dados no banco.
                        O desenvolvedor back-end sempre está capacitado e é competente para solucionar problemas que estão por “trás” do site. 
                        Algumas das linguagens de programação que fazem parte do dia a dia do desenvolvedor back-end: PHP, JavaScript, Java, Python e C#.<br>
                        O desenvolvedor se envolve com codificação, testes de acessibilidade, programação e até segurança e regras do negócio.
                    </p>
